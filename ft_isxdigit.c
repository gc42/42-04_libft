/*
** In list of hexadecimal used digits ([0-9] and [A-F] and [a-f])
*/
int		ft_isxdigit(int c)
{
	if ((48  <= c && c <= 57) \
		|| (65  <= c && c <= 70) \
		|| (97  <= c && c <= 102))
		return (1);
	else
		return (0);
}
